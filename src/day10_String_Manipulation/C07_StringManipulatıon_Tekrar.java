package day10_String_Manipulation;

public class C07_StringManipulatıon_Tekrar {
    public static void main(String[] args) {

         String str="Javayı sevmeye basladım";

        System.out.println(str.charAt(0));

        System.out.println(str.charAt(22));

        System.out.println(str.toUpperCase().charAt(10));

        System.out.println(str.charAt(23));
    }
}

